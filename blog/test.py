# 商品テーブルのデータ
# 商品コードが1つ目の項目
data1 = [
    ['001', '商品A'],
    ['002', '商品B'],
    ['003', '商品C']
]

# 予定テーブルのデータ
# 商品コードが2つ目の項目
data2 = [
    [10, '001'],
    [20, '002'],
    [30, '003']
]

# 在庫管理テーブルのデータ
# 商品コードが3つ目の項目
data3 = [
    [100, 50, '001'],
    [200, 60, '002'],
    [300, 70, '003']
]

# 各リストを辞書に変換（商品コードの項目をキーとして使用）
dict1 = {d[0]: d[1:] for d in data1}
dict2 = {d[1]: [d[0]] for d in data2}
dict3 = {d[2]: d[:2] for d in data3}

# 商品テーブルの全ての商品コードに対して、予定テーブルと在庫管理テーブルから対応するデータを取得
result = {}
for key in dict1.keys():
    result[key] = dict1.get(key, []) + dict2.get(key, []) + dict3.get(key, [])
